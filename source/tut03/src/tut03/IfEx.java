package tut03;

public class IfEx {

	public static void main(String[] args) {
		// 임의의 점수(정수)생성
		// 점수가 90보다 크면 .100점 보다는 적습니다. 학점은 A학점 입니다.
		// 80보다 크면 b학점
		
		
		int jumsu =88;  //메모리 생성
		if (jumsu>=90) {
			System.out.println("100점보다는 적습니다. \n학점은 A학점 입니다.");
		}else {
			System.out.println("100점 보다는 적습니다.\n학점은 B학점 입니다.");
		}

	}

}
